#!/bin/bash
set -e

# Шоттс: Проверяем наличие Git — фундамент для любого взаимодействия с кодом.
# Бодрийяр: Проверка — это лишь подтверждение существования посредника в мире симулякров.

if command -v git > /dev/null 2>&1;
then
  # Шоттс: Git установлен, версия говорит о зрелости инструмента.
  # Бодрийяр: Версия — знак времени, метка бренда, бренда среди брендов.

  echo "Git установлен: $(git --version)"
  echo "Объявляем имя name.user"

  # Шоттс: Настраиваем имя пользователя — голос в истории.
  # Бодрийяр: Имя — лишь ярлык, маска в бесконечном театре изменений.

  git config --global user.name "Wladimir Sobol"

  echo "Объявляем адрес почты"

  # Шоттс: Почта — средство обратной связи и идентификации.
  # Бодрийяр: Электронная почта — эхо твоего цифрового я в сети зеркал.

  git config --global user.email "s.rimidalw@gmail.com"

  # Шоттс: Создаём рабочую директорию — новый контейнер смыслов.
  # Бодрийяр: Контейнер пуст, но обещает наполнение смыслом.

  mkdir -p git-config-example
  cd git-config-example || { echo "Невозможно перейти в каталог"; exit 1; }

  # Шоттс: Инициализируем репозиторий — рождение новой истории.
  # Бодрийяр: История пустая, но жаждет заполнения.

  git init --quiet

  echo "Привет, мир!" > example.txt

  # Шоттс: Добавляем файл — первый акт творения.
  # Бодрийяр: Символический жест в бездне кода.

  git add example.txt

  # Шоттс: Показываем текущие настройки Git — зеркало настроек пользователя.
  # Бодрийяр: Настройки — не что иное, как декорации в пьесе, где ты главный актер.

  git config --list --show-origin

  cd ..
else
  # Шоттс: Git отсутствует — приглашение к действию.
  # Бодрийяр: Отсутствие — начало подлинности.

  echo "Где Git???"
fi




